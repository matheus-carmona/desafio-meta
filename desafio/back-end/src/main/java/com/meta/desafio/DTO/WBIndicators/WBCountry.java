package com.meta.desafio.DTO.WBIndicators;

public class WBCountry {	
	private String id;
	private String content;
	
	public WBCountry() {
		super();
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String get() {
		return content;
	}
	public void setContent(String value) {
		this.content = value;
	}
	
}
